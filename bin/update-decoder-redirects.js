#!/usr/bin/env node

/**
 * Generates docs/lib/decoder-redirects.ts from MDX source files.
 *
 * Scans all .mdx files in docs/content/docs/api/, extracts <DecoderSig> names
 * and aliases, and maps them (lowercased) to their documentation URL.
 *
 * Usage: node bin/update-decoder-redirects.js
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const DOCS_API = path.join(ROOT, 'docs', 'content', 'docs', 'api');
const OUTPUT = path.join(ROOT, 'docs', 'lib', 'decoder-redirects.ts');

// Map: lowercased name -> /docs/api/{slug}#{anchor}
const redirects = new Map();

const mdxFiles = fs
  .readdirSync(DOCS_API)
  .filter((f) => f.endsWith('.mdx'))
  .sort();

// Regex to match self-closing <DecoderSig .../> and <Sig .../> tags (possibly multiline)
const decoderSigRe = /<(?:DecoderSig|Sig)\b([\s\S]*?)\/>/g;
const nameAttrRe = /name="([^"]+)"/;
const aliasNameRe = /name:\s*'([^']+)'/g;

// Regex to find ## and ### headings
const headingRe = /^#{2,3} (.+)/gm;

for (const file of mdxFiles) {
  const slug = file.replace(/\.mdx$/, '');
  const content = fs.readFileSync(path.join(DOCS_API, file), 'utf8');

  // Build a list of heading positions so we can find which heading
  // a <DecoderSig> falls under
  const headings = [];
  let hMatch;
  while ((hMatch = headingRe.exec(content)) !== null) {
    // Remove markdown escapes like \_ -> _ and lowercase
    // Must match the slug function in docs/source.config.ts
    const anchor = hMatch[1]
      .replace(/\\_/g, '_')
      .toLowerCase()
      .replace(/[()]/g, '')
      .replace(/\s+/g, '-')
      .replace(/[^a-z0-9.\-]/g, '')
      .replace(/-{2,}/g, '-')
      .replace(/^-|-$/g, '');
    headings.push({ offset: hMatch.index, anchor });
  }

  /** Find the anchor for a given character offset */
  function anchorAt(offset) {
    let anchor = null;
    for (const h of headings) {
      if (h.offset > offset) break;
      anchor = h.anchor;
    }
    return anchor;
  }

  // Find all <DecoderSig .../> and <Sig .../> tags and extract names + aliases
  let tagMatch;
  while ((tagMatch = decoderSigRe.exec(content)) !== null) {
    const attrs = tagMatch[1];
    const anchor = anchorAt(tagMatch.index);
    if (!anchor) continue;

    const target = `/docs/api/${slug}#${anchor}`;

    // Primary name
    const nm = attrs.match(nameAttrRe);
    if (nm) {
      const name = nm[1].toLowerCase();
      redirects.set(name, target);

      // For instance methods like ".verify", also register "decoder.verify"
      // and a redirect for the page itself (e.g. "decoder" → /docs/api/decoder)
      if (name.startsWith('.')) {
        redirects.set(slug.toLowerCase() + name, target);
        if (!redirects.has(slug.toLowerCase())) {
          redirects.set(slug.toLowerCase(), `/docs/api/${slug}`);
        }
      }
    }

    // Alias names
    let aliasMatch;
    while ((aliasMatch = aliasNameRe.exec(attrs)) !== null) {
      redirects.set(aliasMatch[1].toLowerCase(), target);
    }
  }
}

// Aliases: each key resolves through the current redirects map, so if the
// target is ever renamed the alias follows automatically.
const ALIASES = {
  // Legacy names
  '.then': '.chain',
  'decoder.then': 'decoder.chain',

  // Underscore-free aliases for JS reserved words
  enum: 'enum_',
  null: 'null_',
  undefined: 'undefined_',
};

for (const [alias, target] of Object.entries(ALIASES)) {
  const resolved = redirects.get(target);
  if (resolved) {
    redirects.set(alias, resolved);
  }
}

// Sort entries alphabetically
const sorted = [...redirects.entries()].sort(([a], [b]) => a.localeCompare(b));

// Generate output
const lines = [
  '// Auto-generated by bin/update-decoder-redirects.js — do not edit manually',
  'export const DECODER_REDIRECTS: Record<string, string> = {',
];

for (const [name, target] of sorted) {
  lines.push(`  ${JSON.stringify(name)}: ${JSON.stringify(target)},`);
}

lines.push('};');
lines.push('');

const output = lines.join('\n');
const existing = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT, 'utf8') : '';

if (output === existing) {
  console.log('Decoder redirects are up to date.');
} else {
  fs.writeFileSync(OUTPUT, output);
  console.log(`Updated ${path.relative(ROOT, OUTPUT)}`);
}
